using FluentAssertions;
using MesTech.Application.Features.Crm.Queries.GetLeadScore;
using Xunit;

namespace MesTech.Tests.Unit.Application.Validators.Crm;

[Trait("Category", "Unit")]
[Trait("Feature", "Validators")]
public class GetLeadScoreValidatorTests
{
    private readonly GetLeadScoreValidator _sut = new();

    [Fact]
    public async Task ValidInput_ShouldPass()
    {
        var input = CreateValidQuery();
        var result = await _sut.ValidateAsync(input);
        result.IsValid.Should().BeTrue();
    }

    [Fact]
    public async Task EmptyTenantId_ShouldFail()
    {
        var input = CreateValidQuery() with { TenantId = Guid.Empty };
        var result = await _sut.ValidateAsync(input);
        result.IsValid.Should().BeFalse();
        result.Errors.Should().Contain(e => e.PropertyName == "TenantId");
    }

    [Fact]
    public async Task EmptyLeadId_ShouldFail()
    {
        var input = CreateValidQuery() with { LeadId = Guid.Empty };
        var result = await _sut.ValidateAsync(input);
        result.IsValid.Should().BeFalse();
        result.Errors.Should().Contain(e => e.PropertyName == "LeadId");
    }

    private static GetLeadScoreQuery CreateValidQuery() => new(TenantId: Guid.NewGuid(), LeadId: Guid.NewGuid());
}
